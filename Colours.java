/**
 * A class for coloured and stylized console output
 * @author jacksprayson
 */
public class Colours {
	public static final String RESET = "\033[0m"; //Reset output to default colour
	public static final String BOLD = "\033[1m"; //Make text bold
	public static final String UNDERLINE = "\033[4m"; //Underline text
	public static final String INVERSE = "\033[7m"; //Invert text colour and background colour
	public static final String BOLDOFF = "\033[21m"; //End bold
	public static final String UNDERLINEOFF = "\033[24m"; //Stop underlining
	public static final String INVERSEOFF = "\033[27m"; //End inversion
	public static final String BLACK = "\033[30m"; //Make text black
	public static final String RED = "\033[31m"; //Make text red
	public static final String GREEN = "\033[32m"; //Make text green
	public static final String YELLOW = "\033[33m"; //Make text yellow
	public static final String BLUE = "\033[34m"; //Make text blue
	public static final String MAGENTA = "\033[35m"; //Make text magenta
	public static final String CYAN = "\033[36m"; //Make text cyan
	public static final String WHITE = "\033[37m"; //Make text white
	public static final String ONBLACK = "\033[40m"; //Make background black
	public static final String ONRED = "\033[41m"; //Make background red
	public static final String ONGREEN = "\033[42m"; //Make background green
	public static final String ONYELLOW = "\033[43m"; //Make background yellow
	public static final String ONBLUE = "\033[44m"; //Make background blue
	public static final String ONMAGENTA = "\033[45m"; //Make background magent
	public static final String ONCYAN = "\033[46m"; //Make background cyan
	public static final String ONWHITE = "\033[47m"; //Make background white
}

